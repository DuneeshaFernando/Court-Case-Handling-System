        String nic=jTextField1.getText();
        String name=jTextField2.getText();
        String address=jTextField3.getText();
        
        
        
        String nationality=jTextField4.getText();
        String civilstatus=null;
        String occupation=jComboBox1.getSelectedItem().toString();
        String contactnum=jTextField5.getText();
        String email=jTextField6.getText();
        String password="4a7d1ed414474e4033ac29ccb8653d9b"; //hash of default password at the very beginning ; default password is 0000
        
        
        if(jRadioButton1.isSelected()){
            civilstatus=jRadioButton1.getText();
        }else if(jRadioButton2.isSelected()){
            civilstatus=jRadioButton2.getText();
        }
        
        if(nic.equals("")|name.equals("")|address.equals("")|nationality.equals("")|civilstatus==null|contactnum.equals("")){
            JOptionPane.showMessageDialog(null, "Please make sure that all fields are filled", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Date dob=jDateChooser1.getDate();
                SimpleDateFormat sdf= new SimpleDateFormat("yyyy-MM-dd");
                String bday=sdf.format(dob);
                
                Connection c=dbcon.con();
                Statement s=c.createStatement();
                s.executeUpdate("insert into user values('"+nic+"','"+name+"','"+address+"','"+bday+"','"+nationality+"','"+civilstatus+"','"+occupation+"','"+contactnum+"','"+email+"','"+password+"')");

                ActivityLog.activity_log(MainInter_Registrar.uname,"User registration",nic);
                JOptionPane.showMessageDialog(null, "Successfully registered new user", "Success", JOptionPane.INFORMATION_MESSAGE);
                clear_UI();
            }catch(NullPointerException npe){
                JOptionPane.showMessageDialog(null, "Please make sure that all fields are filled", "Error", JOptionPane.ERROR_MESSAGE);
            }catch(Exception e){
                e.printStackTrace();
            }
        }
