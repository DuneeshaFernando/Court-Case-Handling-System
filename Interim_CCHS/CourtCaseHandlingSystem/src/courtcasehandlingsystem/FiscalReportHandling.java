    void fiscalReportDueList(){
        DefaultListModel dlm=new DefaultListModel();
        try {
            Statement s=dbcon.con().createStatement();
            ResultSet r=s.executeQuery("select * from casedata where status='FiscalReport' ");
            while(r.next()){
                String caseno = r.getString("casenum");
                dlm.addElement(caseno);
            }
            jList1.setModel(dlm);   
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
