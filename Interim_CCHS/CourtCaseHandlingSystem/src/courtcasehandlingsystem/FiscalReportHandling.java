    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String caseNum= jTextField1.getText();
       String fiscalStatus= null;

       if(jRadioButton1.isSelected()){
           fiscalStatus= "success";
       }
       else if(jRadioButton2.isSelected()){
           fiscalStatus="unsuccess";
       }
              
       String reason=jTextArea1.getText();
              
       if(caseNum.equals("")){
           JOptionPane.showMessageDialog(null, "Please select a case number", "Error", JOptionPane.ERROR_MESSAGE); 
       }else if(fiscalStatus==null){
           JOptionPane.showMessageDialog(null, "Please select status of summons", "Error", JOptionPane.ERROR_MESSAGE);
       }else if(fiscalStatus.equals("unsuccess") & reason.equals("")){
           JOptionPane.showMessageDialog(null, "Please enter a reason indicating inability to serve summons", "Error", JOptionPane.ERROR_MESSAGE);   
       }else{
           try {
                Connection c=dbcon.con();
                Statement s=c.createStatement();

                if(fiscalStatus=="success") {
                    s.executeUpdate("UPDATE casedata SET status='defendantinformed' WHERE casenum='"+caseNum+"'");
                }
                else if(fiscalStatus=="unsuccess"){
                    s.executeUpdate("UPDATE casedata SET status='dssummon' WHERE casenum='"+caseNum+"'");
                    s.executeUpdate("insert into fiscalreport values ('"+caseNum+"','"+reason+"')");
                }
                ActivityLog.activity_log(MainInter_subjectClerk.uname,"Status of Fiscal summon",caseNum);
                JOptionPane.showMessageDialog(null, "Summon status was successfully recorded", "Success", JOptionPane.INFORMATION_MESSAGE);
                fiscalReportDueList();
                clear_UI();
            }catch(Exception e){}
       }
    }//GEN-LAST:event_jButton1ActionPerformed

