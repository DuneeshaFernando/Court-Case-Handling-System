    void assetsCeasingList(){
        DefaultListModel dlm=new DefaultListModel();
        try {
            Statement s=dbcon.con().createStatement();
            ResultSet r=s.executeQuery("SELECT * FROM casedata WHERE status='ceaseAssets'");
            while(r.next()){
                String casenumber=r.getString("casenum");
                dlm.addElement(casenumber);
            }
            jList1.setModel(dlm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }
    
