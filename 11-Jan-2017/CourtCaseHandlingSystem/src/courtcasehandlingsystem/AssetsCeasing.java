    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String casenum = jLabel20.getText();
        
        Date today=new Date();
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
        String todayDate=sdf.format(today);
        
        String profession= null;
        if(jRadioButton3.isSelected()){
            profession="governmentOfficer";
        }else{
            profession="nonGovernmentOfficer";
        }
        
        try {
            Connection c=dbcon.con();
            Statement s=c.createStatement();
            s.executeUpdate("UPDATE casedata SET status='case over' WHERE casenum='"+casenum+"'");
            s.executeUpdate("UPDATE specialdates SET writIssuedDate='"+todayDate+"',writDecision='"+profession+"' WHERE casenum='"+casenum+"'");
            ActivityLog.activity_log(MainInter_Registrar.uname,"Assets Ceased",casenum);
            JOptionPane.showMessageDialog(null, "Writ to Deduct Salary of Government Officer will be sent to print", "Print", JOptionPane.INFORMATION_MESSAGE);
            clear_UI();
            assetsCeasingList();
        }catch(Exception e){
            e.printStackTrace();
        }        
    }//GEN-LAST:event_jButton5ActionPerformed

