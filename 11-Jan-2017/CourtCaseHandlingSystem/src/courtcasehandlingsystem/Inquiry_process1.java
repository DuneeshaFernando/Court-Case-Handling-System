    void pendingInquiryList(){
        DefaultListModel dlm=new DefaultListModel();
        try {
            Statement s=dbcon.con().createStatement();
            ResultSet r=s.executeQuery("select inq.* from casedata c,inquiry inq where c.status='pending Inquiry' AND c.casenum=inq.caseNumber");
            while(r.next()){
                String caseno=r.getString("caseNumber");
                String decreeIssueDate_str=r.getString("decreeIssueDate");
                
                DateFormat format=new SimpleDateFormat("yyyy-MM-dd");
                Date decreeIssueDate=format.parse(decreeIssueDate_str);
                int inquiryDuration=calculate_days(decreeIssueDate);
//                System.out.println(caseno+"-"+inquiryDuration);
                if(inquiryDuration<14){
                    dlm.addElement(caseno);      
                }//else update case status to ceaseassets
            }   
            jList1.setModel(dlm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

