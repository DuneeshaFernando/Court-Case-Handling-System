    int calculate_days(Date decreeIssueDate){
        Date today=new Date();
        long today_millisec=today.getTime();
        long registred_Date_millisec=decreeIssueDate.getTime();
        int difference=(int)(today_millisec-registred_Date_millisec)/(24*60*60*1000);
        
        return difference;
    }
    
