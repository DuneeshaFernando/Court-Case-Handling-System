    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String caseNumber=jTextField1.getText();
        
        Date issue_date=jDateChooser1.getDate();
        SimpleDateFormat sdf= new SimpleDateFormat("yyyy-MM-dd");
        String str_issue_date=sdf.format(issue_date);
        
        String curState="pending Inquiry";
      
        try {
            Connection c=dbcon.con();
            Statement s=c.createStatement();
            s.executeUpdate("UPDATE casedata SET status='"+curState+"' WHERE casenum='"+caseNumber+"'");
            s.executeUpdate("UPDATE inquiry SET decreeIssueDate='"+str_issue_date+"' WHERE caseNumber='"+caseNumber+"'");
            ActivityLog.activity_log(MainInter_subjectClerk.uname,"Decree issued date recorded",caseNumber);
            JOptionPane.showMessageDialog(null, "Decree issued date was successfully recorded", "Print", JOptionPane.INFORMATION_MESSAGE);
            HandleDecreeList();
        }catch(Exception e){
        e.printStackTrace();
        }   
    }//GEN-LAST:event_jButton1ActionPerformed


