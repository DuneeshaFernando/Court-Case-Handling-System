    void HandleDecreeList(){
        DefaultListModel dlm=new DefaultListModel();
        try {
            Statement s=dbcon.con().createStatement();
            ResultSet r=s.executeQuery("select * from casedata where status='decreeHandling' ");
            while(r.next()){
                    String caseno=r.getString(1);
                    dlm.addElement(caseno);              
            }
            jList1.setModel(dlm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

