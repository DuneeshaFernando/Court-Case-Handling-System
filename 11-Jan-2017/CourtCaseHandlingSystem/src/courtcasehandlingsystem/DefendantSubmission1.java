    private void jTextField12KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField12KeyPressed
        //0 to 9 -> 48 to 57, control characters -> 0 to 31, DEL -> 127
        if((evt.getKeyCode()>=48 & evt.getKeyCode()<=57) | (evt.getKeyCode()>=0 & evt.getKeyCode()<=31) | evt.getKeyCode()==127 ){
        }else{
            JOptionPane.showMessageDialog(null, "Please enter only numbers", "Error", JOptionPane.ERROR_MESSAGE);
            //following part of code is to erase the last character from text field
            String lawnum=jTextField12.getText();
            int lawnum_len=lawnum.length();
            String reset_tf=lawnum.substring(0, lawnum_len-1);
            jTextField12.setText(reset_tf);
        }
    }//GEN-LAST:event_jTextField12KeyPressed

