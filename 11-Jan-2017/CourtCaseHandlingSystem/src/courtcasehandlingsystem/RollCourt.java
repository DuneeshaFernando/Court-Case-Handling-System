    void todayRollCourtList(){
        Date d=new Date();
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
        String today=sdf.format(d);
        jLabel6.setText(today);
        
        
        DefaultListModel dlm=new DefaultListModel();
        try {
            Statement s=dbcon.con().createStatement();
            ResultSet r=s.executeQuery("SELECT * FROM casedata WHERE status='defendantinformed'");
            while(r.next()){
                String curdate=r.getString("hearingDate");
                if(curdate.equals(today)){
                    String casenumber=r.getString("casenum");
                    dlm.addElement(casenumber);
                }
            }
            jList1.setModel(dlm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }
    
