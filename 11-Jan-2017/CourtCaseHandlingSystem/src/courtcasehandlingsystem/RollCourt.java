    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String casenum=jList1.getSelectedValue().toString();
        String judgename=jComboBox1.getSelectedItem().toString();
        String date=jLabel6.getText();
        String result="Exparty";
        String res_descri=jTextArea1.getText();
        
        if(res_descri.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the ex-parte judgement", "Error", JOptionPane.ERROR_MESSAGE); 
        }else{
            String jnum=null;
            try {
                ResultSet rs=dbcon.con().createStatement().executeQuery("SELECT * FROM judge WHERE name='"+judgename+"'");
                while(rs.next()){
                    jnum=rs.getString("judgeNumber");
                }
            } catch (Exception e) {
            }

            String curState="issueDecree";
            try {
                Connection c=dbcon.con();
                Statement s=c.createStatement();
                s.executeUpdate("UPDATE casedata SET status='"+curState+"' WHERE casenum='"+casenum+"'");
                s.executeUpdate("insert into decision values('"+casenum+"','"+jnum+"','"+date+"','"+result+"','"+res_descri+"')");
                ActivityLog.activity_log(MainInter_courtclerk.uname,"Ex-parte judgement recorded",casenum);
                JOptionPane.showMessageDialog(null,"Ex parte judgement was succesfully recorded", "Success", JOptionPane.INFORMATION_MESSAGE); 
                todayRollCourtList();
                clear_UI();
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

