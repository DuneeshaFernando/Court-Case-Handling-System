    private void jTextField7KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField7KeyPressed
         //0 to 9 -> 48 to 57, control characters -> 0 to 31, DEL -> 127, . -> 46
        if((evt.getKeyCode()>=48 & evt.getKeyCode()<=57) | (evt.getKeyCode()>=0 & evt.getKeyCode()<=31) | evt.getKeyCode()==127 | evt.getKeyCode()==46){
        }else{
            JOptionPane.showMessageDialog(null, "Please enter only numbers and '.' to seperate cents", "Error", JOptionPane.ERROR_MESSAGE);
            //following part of code is to erase the last character from text field
            String val=jTextField7.getText();
            int val_len=val.length();
            String reset_tf=val.substring(0, val_len-1);
            jTextField7.setText(reset_tf);
        }
    }//GEN-LAST:event_jTextField7KeyPressed

