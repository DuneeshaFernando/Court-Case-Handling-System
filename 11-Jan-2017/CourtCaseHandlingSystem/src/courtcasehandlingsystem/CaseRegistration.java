    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String type=jComboBox1.getSelectedItem().toString();
        
        Date d=new Date();
        SimpleDateFormat sdf=new SimpleDateFormat("yy");
        year=sdf.format(d);
            
        try {
            String snewnum = null;
            String setnewnum=null;
            Statement s=dbcon.con().createStatement();
            ResultSet r=s.executeQuery("select * from casedata where type='"+type+"' and caseYear='"+year+"'");
            
            //System.out.println(! r.isBeforeFirst()); //if answer is true, resultset is empty
            if(! r.isBeforeFirst()){
                newnum=1;
                snewnum=Integer.toString(newnum);
            }else{
                try{
                    Statement st=dbcon.con().createStatement();
                    ResultSet rs=st.executeQuery("select MAX(tempcasenum) from casedata where type='"+type+"' and caseYear='"+year+"'");
                    while(rs.next()){
                        String smaxnum=rs.getString("MAX(tempcasenum)");
                        int maxnum=Integer.parseInt(smaxnum);
                        newnum=maxnum+1;
                        snewnum=Integer.toString(newnum);
                    }
                }catch(Exception e){}
            }
                        
            switch (type) {
                case "Hire Purchase":
                    setnewnum="DHP-"+snewnum+"-"+year;
                    break;
                case "Money Matters":
                    setnewnum="DMR-"+snewnum+"-"+year;
                    break;
                case "Money Summary":
                    setnewnum="DMS-"+snewnum+"-"+year;
                    break;
                case "Debt Recovery":
                    setnewnum="DDR-"+snewnum+"-"+year;
                    break;
                case "Mortgage Bond":
                    setnewnum="DMB-"+snewnum+"-"+year;
                    break;
                default:
                    break;
            }

            jTextField15.setText(setnewnum);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

